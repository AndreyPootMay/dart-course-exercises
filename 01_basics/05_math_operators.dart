/**
 * Un operador es un símbolo que le indique al compilador
 * una operación matemática, relacional o lógica.
 */
main() {
  int a = 10 + 5; // Suma
  a = 20 - 10; // Resta
  a = 10 * 2; // Multiplicación

  double b = 10 / 2; // División
  b = 10.0 % 3; // Módulo
  b = -b; // Negación es usado para cambiar el valor de la expresión.

  int c = 10 ~/ 3; // Divisón común y corriente pero solo la parte entera

  int d = 1;

  d++; // Incrementar 1 a el valor
  d--; // Sustraer 1 a el valor

  d+= 2; // Incremento de 2 a el valor
  d-= 2; // Decremento de 2 a el valor
}